      newNode.className = styles['green-text'];
      range.surroundContents(newNode);

      fetch('http://localhost:5000/api/copiedtext', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ text: selection }),
      })
